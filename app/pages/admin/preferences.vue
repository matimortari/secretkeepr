async function handleSubmit(index: number) {
  userStore.errors.updateUser = null
  if (!user.value.id)
    return
  if (!user.value.name) {
    userStore.errors.updateUser = "User name cannot be empty."
    return
  }

  try {
    await userStore.updateUser({
      name: user.value.name,
    })
    await userStore.getUser()
    saveIcon[index]?.triggerSuccess()
  }
  catch (err: any) {
    console.error("Failed to update user data:", err)
    userStore.errors.updateUser = err.message
  }
}

async function handleDeleteUser() {
  userStore.errors.deleteUser = null
  if (!confirm("Are you sure you want to delete your account? This action cannot be undone."))
    return

  try {
    await userStore.deleteUser()
    clear()
    await router.push("/")
  }
  catch (err: any) {
    console.error("Failed to delete account:", err)
    userStore.errors.deleteUser = err.message
  }
}

